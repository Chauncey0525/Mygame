/** 根据ID获取角色 */
export function getCharacterById(id: string): BattleCharacter | undefined {
  return ALL_CHARACTERS.find((c) => c.id === id);
}

/** 获取随机角色 */
export function getRandomCharacter(excludeIds: string[] = []): BattleCharacter {
  const available = ALL_CHARACTERS.filter((c) => !excludeIds.includes(c.id));
  return available[Math.floor(Math.random() * available.length)];
}
